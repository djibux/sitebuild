<h2>Identifier sa version de ZTE Open C</h2>

<p>
    Il existe en France deux versions du ZTE Open C selon leur origine d'achat&nbsp;:
</p>
<ul>
    <li>Les versions achetées sur des sites tels que LDLC, Leclerc, Materiel.net, etc. sont appelées <strong>FR</strong>.</li>
    <li>Les versions achetées sur eBay sont appellées versions <strong>EU</strong>.</li>
</ul>
<p>
    Les différences sont minimes (capteur de luminosité sur la version FR, appareil photo légèrement différent, etc.) mais nécessitent de choisir la bonne version des builds communautaires pour éviter tout problème de compatibilité.
<br />
Pour identifier sa version de téléphone, il suffit d'aller dans les <em>Paramètres</em> puis dans le menu <em>Informations</em>.  Le champ <em>Logiciel</em> affiché commence par&nbsp;:
</p>
<ul>
    <li><kbd>FFOS_FR_ZTE_OPENC</kbd> pour les versions <strong>FR</strong></li>
    <li><kbd>FFOS_EU_EBAY_OPENC</kbd> pour les versions <strong>EU</strong></li>
</ul>
